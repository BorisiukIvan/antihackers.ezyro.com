    <h2 id='begin'>История появления сайта</h2>
    <p>Этот сайт был создан <i>11 сентября 2022 года</i>. Тогда я думал о том, чтобы сделать настоящее общество Антихакеров. Я надеялся, что мы будем работать по-настоящему, то есть узнавать и ликвидировать фишинговые сайты, узнавать и ликвидировать читеров. Ведь мегароботвский АК никак не может похвастаться этим. И я допустил ошибку. Я мечтал о Антихакерах, но <b>я за эти месяцы даже не пытался ничего сделать</b>! Теперь-то я понимаю, что надо было написать программу (я даже теперь знаю, как это сделать), которая будет заниматся Антихакерством автоматически - сразу была бы популярность.</p>
    <p>Где-то сайт оправдал мои ожидания, где-то нет. Однако минусы сайта в том, что здесь очень мало человек активны. Слишком мало. Но с другой стороны, согласитесь, здесь очень хорошо!</p>
    <h2 id='compare'>Сравнение сайта сейчас и тогда</h2>
    <p>Начиная где-то с 12 сентября, все запросы, кроме тех что с моего IP, начали записыватся. Конечно, вначале был какой-то всплеск активности (так всегда), но затем частота запросов уменьшилась. Не знаю, помнит ли кто-то старый сайт. Думаю, только самые старые олды - <a href='/@/darkshark'>@darkshark</a>, зарегистрированная 15 сентября, и <a href='/@/not_found/'>@not_found</a>, зарегистрированный 19-го еще видели тот сайт, который был в то время. Напоминаю, что же там было. Не было ни новостей, ни чата, ни блога, ни шахмат, ни конечно примеров. Личных сообщений и лучших игроков, естественно, не было тоже.</p>
    <p>Вообще я думаю, что если сравнить старый и новый сайты, то выиграет, конечно же новый. Однако у него один существенный недостаток: нету того, что написано на главной странице. Никаких ПО, никаких баз у нас пока что нету. Еще многие говорят, что у сайта "плохое оформление", однако я с ними не согласен. А что вы думаете по этому поводу?</p>
