    this.options = options;

	this.canvas = options.canvas;

	this.ctx = this.canvas.getContext("2d");

	this.colors = options.colors;

	this.draw = function(){

		var total_value = 0;

		var color_index = 0;

		for (var categ in this.options.data){

			var val = this.options.data[categ];

			total_value += val;

		}

		var start_angle = 0;

