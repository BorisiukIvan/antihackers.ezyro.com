    }
    function drawPieSlice(ctx,centerX, centerY, radius, startAngle, endAngle, color ){

    ctx.fillStyle = color;

	ctx.beginPath();

	ctx.moveTo(centerX,centerY);

	ctx.arc(centerX, centerY, radius, startAngle, endAngle);

	ctx.closePath();

	ctx.fill();

    }
